<h1 class="filterTitle  on-med-and-down" dir="rtl">دیدنی های استان فارس</h1>
<br>
<p class="center" lang="fa" dir="rtl">
    <b>شیراز</b> 
     <a href="/wiki/%D9%81%D9%87%D8%B1%D8%B3%D8%AA_%DA%A9%D9%84%D8%A7%D9%86%E2%80%8C%D8%B4%D9%87%D8%B1%D9%87%D8%A7%DB%8C_%D8%A7%DB%8C%D8%B1%D8%A7%D9%86" title="فهرست کلان‌شهرهای ایران">کلان‌شهری</a> در ایران و مرکز 
     <a href="/wiki/%D8%A7%D8%B3%D8%AA%D8%A7%D9%86_%D9%81%D8%A7%D8%B1%D8%B3" title="استان فارس">استان فارس</a> 
     در جنوب کشور است. جمعیت کلانشهر شیراز در برآورد سال ۱۴۰۰ خورشیدی، بالغ بر ۱،۹۵۵،۵۰۰ تن بوده است که بر این اساس شیراز چهارمین شهر بزرگ و پرجمعیت ایران و پرجمعیت‌ترین شهر جنوب کشور به‌شمار می‌رود.
     <sup id="cite_ref-5" class="reference"><a href="#cite_note-5"></a> 
</p>
<br>
<p  lang="fa" dir="rtl">
<p  dir="rtl">شیراز در بخش مرکزی 
  <a href="/wiki/%D8%A7%D8%B3%D8%AA%D8%A7%D9%86_%D9%81%D8%A7%D8%B1%D8%B3" title="استان فارس">استان فارس</a>
  ، در ارتفاع ۱۴۸۶ متر از سطح دریا و در منطقهٔ کوهستانی 
  <a href="/wiki/%D8%B2%D8%A7%DA%AF%D8%B1%D8%B3" title="زاگرس">زاگرس</a> 
  واقع شده و آب و هوای معتدلی دارد. این شهر، از سمت غرب به 
</p>
<p  dir="rtl">
  <a href="/wiki/%DA%A9%D9%88%D9%87_%D8%AF%D8%B1%D8%A7%DA%A9" title="کوه دراک">کوه دراک</a>
  ، از سمت شمال به کوه‌های <a href="/wiki/%D8%A8%D9%85%D9%88_(%DA%A9%D9%88%D9%87)" class="mw-redirect" title="بمو (کوه)">بمو</a>، 
  <a href="/wiki/%D8%B3%D8%A8%D8%B2%D9%BE%D9%88%D8%B4%D8%A7%D9%86_(%DA%A9%D9%88%D9%87)" title="سبزپوشان (کوه)">سبزپوشان</a>، 
  <a href="/wiki/%DA%86%D9%87%D9%84%E2%80%8C%D9%85%D9%82%D8%A7%D9%85_(%DA%A9%D9%88%D9%87)" title="چهل‌مقام (کوه)">چهل‌مقام</a> و 
  <a href="/wiki/%D8%A8%D8%A7%D8%A8%D8%A7%DA%A9%D9%88%D9%87%DB%8C_(%DA%A9%D9%88%D9%87)" title="باباکوهی (کوه)">باباکوهی</a> (از رشته‌کوه‌های 
  <a href="/wiki/%D8%B2%D8%A7%DA%AF%D8%B1%D8%B3" title="زاگرس">زاگرس</a>) محدود شده‌است. 
</p>
